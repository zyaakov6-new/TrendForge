// ─── Helper: format ISO date as human-readable ────────────────────────────────

function formatResolveDate(iso: string): string {
  try {
    return new Date(iso + "T12:00:00Z").toLocaleDateString("en-US", {
      month: "short",
      day: "numeric",
      year: "numeric",
    });
  } catch {
    return iso;
  }
}

// ─── Route handler ────────────────────────────────────────────────────────────

export async function POST(req: NextRequest) {

  // ── 1. Rate limiting ──────────────────────────────────────────────────────
  const ip =
    req.headers.get("x-forwarded-for")?.split(",")[0].trim() ??
    req.headers.get("x-real-ip") ??
    "unknown";

  const { ok, retryMs } = rateCheck(ip);
  if (!ok) {
    return NextResponse.json(
      { error: "Too many requests — please wait a moment and try again." },
      { status: 429, headers: { "Retry-After": String(Math.ceil(retryMs / 1000)) } }
    );
  }

  // ── 2. Parse + validate input ─────────────────────────────────────────────
  let body: GenerateMarketRequest;
  try {
    body = (await req.json()) as GenerateMarketRequest;
  } catch {
    return NextResponse.json({ error: "Invalid JSON body." }, { status: 400 });
  }

  const topic = (body.topic ?? "").trim();
  if (!topic) {
    return NextResponse.json(
      { error: "topic is required and must be a non-empty string." },
      { status: 400 }
    );
  }
  if (topic.length > 1_000) {
    return NextResponse.json(
      { error: "topic must be 1 000 characters or fewer." },
      { status: 400 }
    );
  }

  const language  = body.language === "he" ? "he" : "en";
  const extraCtx  = (body.context ?? "").trim().slice(0, 2_000);

  // ── 3. API key guard ──────────────────────────────────────────────────────
  if (!process.env.ANTHROPIC_API_KEY) {
    console.error("[generate-market] ANTHROPIC_API_KEY is not set");
    return NextResponse.json(
      { error: "AI service is not configured. Set ANTHROPIC_API_KEY in your environment." },
      { status: 503 }
    );
  }

  // ── 4. Build user message ─────────────────────────────────────────────────
  const today = new Date().toLocaleDateString("en-US", {
    weekday: "long", year: "numeric", month: "long", day: "numeric",
  });

  const userParts: string[] = [
    `Today is ${today}.`,
    `Input language: ${language === "he" ? "Hebrew - translate question to English for on-chain storage and return Hebrew translation in questionHe" : "English - set questionHe to empty string"}.`,
    "",
    `Topic / trend:`,
    topic,
  ];

  if (extraCtx) {
    userParts.push("", "Additional context:", extraCtx);
  }

  userParts.push(
    "",
    "Generate a high-quality, tradable prediction market. Call create_prediction_market with every field."
  );

  const userMessage = userParts.join("\n");

  // ── 5. Call Claude with forced tool use ───────────────────────────────────
  let toolInput: Record<string, unknown>;

  try {
    const response = await client().messages.create({
      model:      "claude-opus-4-7",
      max_tokens: 2_048,
      thinking:   { type: "adaptive" },
      // Force exactly this tool — Claude cannot respond with plain text
      tool_choice: { type: "tool", name: "create_prediction_market" },
      tools:  [MARKET_TOOL],
      system: [
        {
          type:          "text",
          text:          SYSTEM_PROMPT,
          // Prompt caching: stable system prompt is cached at Anthropic's edge.
          // Saves ~800 input tokens + latency on every call after the first.
          cache_control: { type: "ephemeral" },
        },
      ],
      messages: [{ role: "user", content: userMessage }],
    });

    // With tool_choice forced there is exactly one tool_use block
    const toolBlock = response.content.find(
      (b): b is Anthropic.ToolUseBlock => b.type === "tool_use"
    );

    if (!toolBlock) {
      throw new Error("Claude did not call the expected tool");
    }

    toolInput = toolBlock.input as Record<string, unknown>;

  } catch (err: unknown) {
    console.error("[generate-market] Anthropic error:", err);

    if (err instanceof Anthropic.APIError) {
      if (err.status === 401)
        return NextResponse.json({ error: "Invalid API key. Check ANTHROPIC_API_KEY." }, { status: 503 });
      if (err.status === 529)
        return NextResponse.json({ error: "Claude is overloaded — please retry in a moment." }, { status: 503 });
      if (err.status === 400)
        return NextResponse.json({ error: `Bad request to AI: ${err.message}` }, { status: 400 });
    }

    return NextResponse.json(
      { error: "Failed to generate market — please try again." },
      { status: 500 }
    );
  }

  // ── 6. Coerce + enrich the tool output ───────────────────────────────────

  // Clamp numeric fields defensively
  const yesPrice     = Math.max(1, Math.min(99, Math.round(Number(toolInput.yesPrice)     || 50)));
  const sentiment    = Math.max(1, Math.min(99, Math.round(Number(toolInput.sentimentBullish) || 50)));
  const xPosts       = Math.max(800, Math.min(24_000, Math.round(Number(toolInput.xPostsAnalyzed) || 5_000)));
  const liquidity    = Math.max(1_000, Math.min(50_000, Math.round(Number(toolInput.suggestedLiquidityUSDC) || 5_000)));

  // Normalise category
  const rawCat = String(toolInput.category ?? "");
  const category: MarketCategory = VALID_CATEGORIES.includes(rawCat as MarketCategory)
    ? (rawCat as MarketCategory)
    : "Politics";

  // Ensure endDate is a real date
  const endDate    = String(toolInput.endDate ?? "").match(/^\d{4}-\d{2}-\d{2}$/)
    ? String(toolInput.endDate)
    : new Date(Date.now() + 90 * 86_400_000).toISOString().slice(0, 10);

  const resolveDate = formatResolveDate(endDate);

  // Ensure description has the required prefix
  let description = String(toolInput.description ?? "").trim();
  if (!description.startsWith("[AI-GENERATED")) {
    description = `[AI-GENERATED by TrendForge] ${description}`;
  }

  // Ensure outcomes is a non-empty array
  const outcomes: string[] = Array.isArray(toolInput.outcomes) && toolInput.outcomes.length > 0
    ? (toolInput.outcomes as unknown[]).map(String)
    : ["Yes", "No"];

  const market: GeneratedMarket = {
    question:             String(toolInput.question ?? "").trim(),
    questionHe:           String(toolInput.questionHe ?? "").trim() || null,
    description,
    outcomes,
    resolutionCriteria:   String(toolInput.resolutionCriteria ?? "").trim(),
    category,
    endDate,
    resolveDate,
    yesPrice,
    xPostsAnalyzed:       xPosts,
    sentimentBullish:     sentiment,
    suggestedLiquidityUSDC: liquidity,
    tags:                 Array.isArray(toolInput.tags) ? (toolInput.tags as unknown[]).map(String) : [],
    resolutionSources:    Array.isArray(toolInput.resolutionSources) ? (toolInput.resolutionSources as unknown[]).map(String) : [],
    aiReasoning:          String(toolInput.aiReasoning ?? "").trim(),
  };

  // ── 7. Return ─────────────────────────────────────────────────────────────
  return NextResponse.json({ market }, { status: 200 });
}
