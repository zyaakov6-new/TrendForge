/**
 * POST /api/generate-market
 *
 * Generates a Polymarket/Kalshi-quality prediction market using Claude claude-opus-4-7
 * with TOOL USE forced calling — Claude MUST populate every schema field or the
 * API returns an error. No JSON parsing, no hallucinated structure.
 *
 * Body: { topic: string, language?: "en"|"he", context?: string }
 * Returns: { market: GeneratedMarket }
 *
 * Security:
 *  - ANTHROPIC_API_KEY read from process.env — server-side only, never in client bundle
 *  - In-memory rate limiting: 10 req / IP / minute
 *  - Input length cap: 1 000 chars for topic, 2 000 for context
 */

import Anthropic from "@anthropic-ai/sdk";
import { NextRequest, NextResponse } from "next/server";
import type {
  GeneratedMarket,
  GenerateMarketRequest,
  MarketCategory,
} from "@/types/generated-market";
import { VALID_CATEGORIES } from "@/types/generated-market";

// ─── Rate limiter (in-memory) ─────────────────────────────────────────────────

const WINDOW_MS  = 60_000;
const MAX_PER_IP = 10;

interface Bucket { count: number; resetAt: number }
const buckets = new Map<string, Bucket>();

function rateCheck(ip: string): { ok: boolean; retryMs: number } {
  const now = Date.now();

  // Purge expired buckets to prevent unbounded growth
  if (buckets.size > 1_000) {
    for (const [k, b] of buckets) if (now >= b.resetAt) buckets.delete(k);
  }

  let b = buckets.get(ip);
  if (!b || now >= b.resetAt) {
    b = { count: 0, resetAt: now + WINDOW_MS };
    buckets.set(ip, b);
  }

  if (b.count >= MAX_PER_IP) return { ok: false, retryMs: b.resetAt - now };
  b.count++;
  return { ok: true, retryMs: 0 };
}

// ─── Anthropic client (module-level singleton) ────────────────────────────────

let _client: Anthropic | null = null;
function client(): Anthropic {
  return (_client ??= new Anthropic({ apiKey: process.env.ANTHROPIC_API_KEY }));
}

